// 获取按钮和隐藏文本元素
var toggleButton1 = document.getElementById('showButton1');
var hiddenText1 = document.getElementById('hiddenText1');

// 切换文本的显示状态
toggleButton1.addEventListener('click', function() {
  if (hiddenText1.classList.contains('hidden')) {
    hiddenText1.classList.remove('hidden');
    hiddenText1.classList.add('show');
    toggleButton1.textContent = 'Hide translation';
  } else {
    hiddenText1.classList.remove('show');
    hiddenText1.classList.add('hidden');
    toggleButton1.textContent = 'Show translation';
  }
});

